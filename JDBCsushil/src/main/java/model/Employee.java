package model;

public class Employee {
    public int id;
    public String name;
    public String city;
    public int age;
    public String deptName;

    public Employee(int id, String name, String city, int age, String deptName) {
        this.id = id;
        this.name = name;
        this.city = city;
        this.age = age;
        this.deptName = deptName;
    }



}
